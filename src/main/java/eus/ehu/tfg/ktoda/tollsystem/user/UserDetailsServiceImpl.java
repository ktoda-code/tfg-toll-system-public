package eus.ehu.tfg.ktoda.tollsystem.user;

import lombok.AllArgsConstructor;
import lombok.extern.slf4j.Slf4j;
import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;
import org.springframework.transaction.annotation.Transactional;

import java.util.Optional;

@Service
@AllArgsConstructor
@Slf4j
@Transactional
public class UserDetailsServiceImpl implements UserDetailsService {
    private final UserRepository userRepository;

    @Override
    public UserDetails loadUserByUsername(String username) throws UsernameNotFoundException {
        Optional<User> user = userRepository.findByEmail(username);
        // log.info("Searching user: " + username);
        if (user.isEmpty()) {
            log.error("User not found with email: " + username);
            throw new UsernameNotFoundException("User not found with email: " + username);
        }

        UserDetails userDetail = org.springframework.security.core.userdetails.User.
                withUsername(user.get().getEmail())
                .password(user.get().getPassword())
                .authorities(user.get().getAuthorities())
                .build();
        //log.info("Accessing user: " + userDetail);
        return userDetail;
    }
}
